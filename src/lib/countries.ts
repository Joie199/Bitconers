// Sort countries alphabetically by name
export const sortedCountries = [...allCountries].sort((a, b) => {
  const nameA = a.name.toLowerCase().trim();
  const nameB = b.name.toLowerCase().trim();
  return nameA.localeCompare(nameB, undefined, { sensitivity: 'base', numeric: true });
});

// Get phone rules for a country
export function getPhoneRule(countryName: string): { min: number; max: number } {
  const country = allCountries.find(c => c.name === countryName);
  if (country) {
    return { min: country.minDigits, max: country.maxDigits };
  }
  // Default fallback: use maximum digits any country has (13 for Germany/Austria)
  return { min: 5, max: 13 };
}
